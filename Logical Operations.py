# Using and
x = 5
y = 10
print("Using 'and':", x > 6 and y < 20)  # Result: True, because both conditions are True

# Using or
print("Using 'or':", x == 3 or y > 5)  # Result: True, because the second condition is True

# Using not
z = True
print("Using 'not':", not z)  # Result: False, because 'not True' is False

