my_list = [1,2,3,4,5,6]
print(3 in my_list)  # Result: True, because 3 is in the list
print(6 in my_list)  # Result: False, because 6 is not in the list
my_string = "Hello, world!"
print("H" not in my_string)  # Result: False, because "H" is in the string
print("x" not in my_string)  # Result: True, because "x" is not in the string
