# Addition
result_addition = 5 + 3  # Result: 8

# Subtraction
result_subtraction = 8 - 3  # Result: 5

# Multiplication
result_multiplication = 4 * 2  # Result: 8

# Division
result_division = 10 / 2  # Result: 5.0 (Note: Python 3 performs float division by default)

# Modulus
result_modulus = 10 % 3  # Result: 1 (remainder of 10 divided by 3)

# Exponentiation
result_exponentiation = 2 ** 3  # Result: 8 (2 raised to the power of 3)

print("Addition:", result_addition)
print("Subtraction:", result_subtraction)
print("Multiplication:", result_multiplication)
print("Division:", result_division)
print("Modulus:", result_modulus)
print("Exponentiation:", result_exponentiation)
